#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t i;

	i = 0;
	while (i < ft_strlen(s) && *s != (char)c)
		s++;
	if (*s == c)
		return ((char*)s);
	return (NULL);
}
