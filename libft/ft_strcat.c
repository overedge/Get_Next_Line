#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int l;

	i = 0;
	l = ft_strlen(dest);
	while (src[i] != '\0')
	{
		dest[l + i] = src[i];
		i++;
	}
	dest[l + i] = '\0';
	return (dest);
}
