#include "libft.h"

int		ft_atoi(const char *nptr)
{
	int				i;
	int				res;
	int				sign;
	unsigned char	*str;

	i = 0;
	res = 0;
	sign = 1;
	str = (unsigned char*)nptr;
	while (str[i] && str[i] < 33)
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * sign);
}
